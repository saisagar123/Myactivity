package chapter2;
import java.util.*;
public class BitOper
{
public static void main(String args[])
{
	Scanner sc=new Scanner(System.in);
	 int a =sc.nextInt(); 
     int b = sc.nextInt(); 

     // bitwise and 

     System.out.println("a&b = " + (a & b)); 

     // bitwise or 
      
     System.out.println("a|b = " + (a | b)); 

     // bitwise xor
     
     System.out.println("a^b = " + (a ^ b)); 
     
     //bitwise not
}
}
